package vp.uvodujavu;

public class Pravougaonik {
	
	public static void main(String[] args) {
		double a = 3;
		double b = 2;
		
		double povrsina = a * b;
		double obim = 2 * a + 2 * b;
		
		System.out.println("Povrsina pravougaonika je " + povrsina);
		System.out.println("Obim pravougaonika je " + obim);
	}
}
